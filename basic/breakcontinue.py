for i in range(5):
    if(i==3):
        break  #breaks the loop completely
    print(i)
print('\n')
#example of continue statement
for i in range(5):
    if(i==3):
        continue   #skips the current iteration and moves to next
    print(i)
print('\n')
#example of pass statement
for i in range(5):
    if(i==3):
        pass   #it simply does nothing it is used like a filler if one do not know what to write in that block or suite
    print(i)